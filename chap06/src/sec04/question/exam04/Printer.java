package sec04.question.exam04;
// printer 객체 생성하기
public class Printer {
	void println(int value) {
		System.out.println(value);
	}
	boolean println(boolean value) {
		System.out.println(value);
		return value;
	}
	double println(double value) {
		System.out.println(value);
		return value;
	}
	String println(String value) {
		System.out.println(value);
		return value;
	}
}
