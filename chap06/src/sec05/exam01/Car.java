package sec05.exam01;
// 인스턴스 멤버와 this
public class Car {
	// 필드(Field)
	String model;
	int speed;
	
	// 생성자(Constructor)
	Car(String model) {
		this.model = model;
	}
	
	// 메소드(Method)
	void setSpeed(int speed) {
		this.speed = speed;
	}
	
	void run() {
		for(int i=10; i<50; i+=10) {
			this.setSpeed(i);
			System.out.println(this.model + "가 달립니다. (시속:"+this.speed + "km/h");
		}
	}
}
